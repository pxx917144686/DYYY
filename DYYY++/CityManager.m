            }
            if (districtsInCounty) {
                [chongqingDistricts addEntriesFromDictionary:districtsInCounty];
            }
            if (chongqingDistricts.count > 0) {
                return [chongqingDistricts copy];
            }
        }
        
        // 香港、澳门特别行政区
        if ([prefix isEqualToString:@"81"] || [prefix isEqualToString:@"82"]) {
            districts = _allDistrictsDict[parentCode]; // 尝试用原代码
            if (districts) return districts;
        }
    }

    // 3. 尝试从区县代码反推父级代码
    // 检查是否是重庆的区县代码
    if ([parentCode hasPrefix:@"50"] && parentCode.length > 4) {
        NSString *prefix = [parentCode substringToIndex:4];
        if ([prefix isEqualToString:@"5001"]) {
            return _allDistrictsDict[@"500100"]; // 市区
        } else if ([prefix isEqualToString:@"5002"]) {
            return _allDistrictsDict[@"500200"]; // 县
        }
    }

    // 尝试通用区县反推：取前4位+"00" 作为城市 Key
    if (parentCode.length >= 6) {
        NSString *guessedCityCode = [[parentCode substringToIndex:4] stringByAppendingString:@"00"];
        districts = _allDistrictsDict[guessedCityCode];
        if (districts) {
            return districts; // 返回所属城市的区县列表
        }
    }

    return @{}; // 未找到
}

// 修改 getStreetsInDistrict 方法，保证数据完整性
- (NSArray *)getStreetsInDistrict:(NSString *)districtCode {
    if (!districtCode || districtCode.length < 6) {
        return @[];
    }
    
    // 直接查找区县对应的街道
    NSArray *streets = _allStreetsDict[districtCode];
    if (streets) {
        return streets;
    }
    
    // 对于东莞/中山，传入的可能是镇/街道的代码(>6位)，它们没有下一级街道
    if (districtCode.length > 6 && ([districtCode hasPrefix:@"4419"] || [districtCode hasPrefix:@"4420"])) {
        return @[]; // 没有更下级的数据
    }
    
    // 台湾、香港、澳门的区/堂区，也没有街道数据
    if ([districtCode hasPrefix:@"71"] || [districtCode hasPrefix:@"81"] || [districtCode hasPrefix:@"82"]) {
        return @[];
    }

    if ([districtCode hasPrefix:@"11"] || [districtCode hasPrefix:@"12"] || 
        [districtCode hasPrefix:@"31"] || [districtCode hasPrefix:@"50"]) {
        
        // 获取区县名称
        NSString *districtName = [self getDistrictNameWithCode:districtCode];
        if (districtName) {
            return @[[NSString stringWithFormat:@"%@中心街道", districtName], 
                    [NSString stringWithFormat:@"%@北街道", districtName],
                    [NSString stringWithFormat:@"%@南街道", districtName]];
        }
    }

    return @[]; // 默认返回空数组
}

