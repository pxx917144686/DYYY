// 根据省份代码获取省份名称
- (NSString *)getProvinceNameWithCode:(NSString *)provinceCode {
    if (!provinceCode || provinceCode.length == 0) {
        return nil;
    }
    
    // 优先匹配6位代码
    if (provinceCode.length == 6) {
        NSString *name = _provincesDict[provinceCode];
        if (name) return name;
    }
    // 兼容传入的是城市或区县代码，尝试提取省份代码 (前2位 + "0000")
    if (provinceCode.length >= 2) {
        NSString *prefixCode = [provinceCode substringToIndex:2];
        NSString *guessedProvinceCode = [prefixCode stringByAppendingString:@"0000"];
        return _provincesDict[guessedProvinceCode];
    }
    return nil; // 未找到
}

// 完善 getCityNameWithCode 方法，增强兼容性
- (NSString *)getCityNameWithCode:(NSString *)cityCode {
    if (!cityCode || cityCode.length < 2) {
        return nil;
    }
    
    // 优先直接匹配传入的代码 (可能是6位城市代码, 或xx0100, 或特殊区域代码)
    NSString *name = _citiesDict[cityCode];
    if (name) return name;

    // 如果直接匹配不到，尝试把它当作区县代码处理
    // 尝试获取其所属城市代码 (前4位 + "00")
    if (cityCode.length >= 4) {
        NSString *prefixCode = [cityCode substringToIndex:4];
        NSString *guessedCityCode = [prefixCode stringByAppendingString:@"00"];

        // 检查这个推测的城市代码是否存在于 _citiesDict 的 key 中
        name = _citiesDict[guessedCityCode];
        if (name) return name;

        // 对于直辖市，处理特殊情况
        NSString *provincePrefix = [cityCode substringToIndex:2];
        if ([provincePrefix isEqualToString:@"11"] || 
            [provincePrefix isEqualToString:@"12"] || 
            [provincePrefix isEqualToString:@"31"] || 
            [provincePrefix isEqualToString:@"50"]) {
            NSString *directMunicipalKey = [provincePrefix stringByAppendingString:@"0100"];
            name = _citiesDict[directMunicipalKey];
            if (name) return name; // 返回直辖市名称
        }

        // 处理特殊行政区
        if ([provincePrefix isEqualToString:@"81"] || 
            [provincePrefix isEqualToString:@"82"] || 
            [provincePrefix isEqualToString:@"71"]) {
            NSString *regionKey = [provincePrefix stringByAppendingString:@"0000"];
            name = _provincesDict[regionKey]; // 使用省份字典查询
            if (name) return name;
        }
    }

    // 如果还找不到，尝试返回省份名称（作为最后的兼容）
    return [self getProvinceNameWithCode:cityCode];
}

// 获取区县名称
- (NSString *)getDistrictNameWithCode:(NSString *)districtCode {
    if (!districtCode || districtCode.length < 6) {
        return nil;
    }
    
    // 先尝试直接获取区县名称
    NSString *prefix = [districtCode substringToIndex:4];
    NSString *cityCode = [prefix stringByAppendingString:@"00"];
    NSDictionary *districts = _allDistrictsDict[cityCode];
    NSString *name = districts[districtCode];
    if (name) return name;
    
    // 处理特殊情况：东莞、中山等
    if ([districtCode hasPrefix:@"4419"] || [districtCode hasPrefix:@"4420"]) {
        districts = _allDistrictsDict[districtCode];
        if (districts) {
            return districts[@"district_name"] ?: [districtCode substringFromIndex:6];
        }
    }
    
    // 特殊处理：直辖市
    NSString *provincePrefix = [districtCode substringToIndex:2];
    if ([provincePrefix isEqualToString:@"11"] || 
        [provincePrefix isEqualToString:@"12"] || 
        [provincePrefix isEqualToString:@"31"] || 
        [provincePrefix isEqualToString:@"50"]) {
        
        if ([provincePrefix isEqualToString:@"50"]) {
            // 重庆特殊处理
            if ([districtCode hasPrefix:@"5001"]) {
                districts = _allDistrictsDict[@"500100"];
            } else if ([districtCode hasPrefix:@"5002"]) {
                districts = _allDistrictsDict[@"500200"];
            }
        } else {
            NSString *directMunicipalKey = [provincePrefix stringByAppendingString:@"0100"];
            districts = _allDistrictsDict[directMunicipalKey];
        }
        
        name = districts[districtCode];
        if (name) return name;
    }
    
    // 处理特殊行政区
    if ([provincePrefix isEqualToString:@"81"] || [provincePrefix isEqualToString:@"82"]) {
        districts = _allDistrictsDict[[provincePrefix stringByAppendingString:@"0000"]];
        name = districts[districtCode];
        if (name) return name;
    }
    
    return nil;
}

// 获取街道名称
- (NSString *)getStreetNameWithCode:(NSString *)streetCode {
    if (!streetCode || streetCode.length < 9) {
        return nil;
    }
    
    // 获取区县代码
    NSString *districtCode = [streetCode substringToIndex:6];
    NSArray *streets = _allStreetsDict[districtCode];
    
    // 尝试根据索引获取街道名称
    if (streets && streets.count > 0) {
        int index = [[streetCode substringFromIndex:6] intValue] - 1;
        if (index >= 0 && index < streets.count) {
            return streets[index];
        }
    }
    
    // 处理特殊情况
    if ([streetCode hasPrefix:@"4419"] || [streetCode hasPrefix:@"4420"]) {
        // 东莞和中山的镇街是特殊编码
        NSDictionary *townInfo = _allDistrictsDict[streetCode];
        return townInfo[@"name"];
    }
    
    return nil;
}

